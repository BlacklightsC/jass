/**
 * 种类
 */
const Kind = {
  Unit: 0x00,
  Item: 0x10,
  Destructible: 0x20,
  Ornament: 0x30,
  Ability: 0x40,
  Buff: 0x50,
  Technology: 0x60,
}

/**
 * 转换kind为可读字符串
 * @param {number} kind 
 * @returns {string}
 */
const kindToString = (kind) => {
  switch (kind) {
    case Kind.Unit:
      return "单位";
    case Kind.Item:
      return "物品";
    case Kind.Destructible:
      return "可破坏物";
    case Kind.Ornament:
      return "地形";
    case Kind.Ability:
      return "技能";
    case Kind.Buff:
      return "状态效果";
    case Kind.Technology:
      return "科技树";
    default:
      return "";
  }
}

